def convert_to_txt(data, filename):
    with open(filename, 'w') as f:
        for key, value in data.items():
            f.write(f"{key}:\n")
            if isinstance(value, list):
                for item in value:
                    if isinstance(item, dict):
                        for subkey, subvalue in item.items():
                            f.write(f"  {subkey}: {subvalue}\n")
                    else:
                        f.write(f"  {item}\n")
            else:
                f.write(f"  {value}\n")

